int		ft_tolower(char *str)
{
	int fin;
	int i;

	i = 0;
	fin = 1;
	while (str[i] != '\0' && fin == 1)
	{
		if (str[i] > 'z' || str[i] < 'a')
		{
			fin = 0;
		}
		i++;
	}
	return (fin);
}
